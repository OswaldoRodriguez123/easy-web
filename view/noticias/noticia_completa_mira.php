						<p class="padding-top-20 text-justify">
						Uno de los grande objetivos de Easy dance, es crear un espacio para que todos aquellos alumnos, 
						clientes o usuarios integrados a la plataforma, puedan recibir talleres, tutoriales y webinar de primera línea, 
						con imágenes Hd,entrevistas  con diversos instructores de diferentes áreas, países y estilos del mundo. En el que 
						los alumnos puedan aprender técnicas, tips y compartir experiencias con los instructores que se postulen para la 
						participación de las clases. 
						</p>
						<p class="padding-top-10 text-justify" ><b>¿Quiénes serán los instructores?</b></p>
                        <p class="padding-top-10 text-justify">
						Todos aquellos, instructores, directores o coreógrafos  que manejen  alto nivel de conocimientos con ciertos 
						tópicos del baile, podrán postularse para la ronda de talleres, de esa forma,  el equipo de Easy Dance 
						seleccionará a los instructores según el rango de experiencia, contenido que ofrezcan  y 
						la programación establecida , en el que se compartirán temas de salsa, flamenco, las diversas áreas de la Danzas, tango, 
						bailes de salón, afrocubano, bailes típicos de cuba, danza árabe ,pool dance y otros que pertenezcan al gremio del baile. 
						</p>
                        <p class="padding-top-10 text-justify">
						El equipo de trabajo   de Easy Dance, ha  citado como fecha de  lanzamiento de los talleres, conferencias y webinars,
						para el  mes de abril, en el que abrirá la tanda diversos profesionales que se postulen para luego ser anunciados,
						a través de la plataforma. 
						</p>
                        <p class="padding-top-10 text-justify">
						Los tópicos que busca nuestro equipo en la primera etapa de lanzamiento  son aquellos que generen contenido de alto  valor a todos los usuarios que estén adscritos a la misma.
						</p>
                        <p class="padding-top-10 text-justify">
						Entre los primeros temas que se debatirán son, el buen  dominio de  los tiempos musicales,  coordinación de movimientos, consideraciones para montajes coreográficos, maquillaje de bailarines para los escenarios, entrenamiento y acondicionamiento para bailarines, como evitar el miedo escénico, expresión corporal entre otros. 
						</p>
                                                
