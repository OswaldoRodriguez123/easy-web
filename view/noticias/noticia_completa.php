						<h4 class="padding-top-10 text-center">
							Ahora las academias de baile, instructores y bailarines en latino América  tendrán  su propia aplicación tecnológica en la web con estampa VENEZOLANA.
						</h4>
						<p class="padding-top-20 text-justify">
						En Latinoamérica  el baile ha venido evolucionando a un nivel muy  importante, cada día   son más los emprendedores  que  originan y crean
						sus propias academias, organizaciones o compañías de baile, sin embargo direccionar un proyecto artístico no es nada sencillo, debido a las
						múltiples actividades y compromisos que estos generan, tales como, eventos, talleres, clases particulares y grupales, administración,
						atención al cliente, montaje de 15 años, entre otros,  por esa razón hemos emprendido un proyecto llamado Easy Dance, para brindar respuestas
						a las múltiples operaciones que el gremio artístico requiere. 
						</p>
						
						<div class="col-md-12 padding-top-bottom-20">
							<img class="img-responsive" src="img/software-atencion_cliente-easydance.png"> 
						</div>
						
						<h4 class="padding-top-20">
							Los objetivos de la aplicación Easy Dance
						</h4>
						
						<p class="text-justify">
						 Automatizar los procesos organizativos en todas las áreas del baile que hasta hoy día se hacen complejos debido a la falta de herramientas
						 necesarias, De esta manera, la aplicación tiene como objetivo principal simplificar la gestión y generar controles y estadísticas de los
						 procesos. 
						</p>
						
						<h4 class="padding-top-20">
							A quien va dirigido
						</h4>
						
						<p class="text-justify">
						 Lo interesante y la gran fortaleza que tiene Easy dance es que no sólo va dirigido a los directores de academias de baile para gestionar
						 sus procesos, sino que involucra al gremio en general, alumnos, bailarines, familiares e instructores, dándole la importancia a cada 
						 persona que participa directa o indirectamente en el mundo del baile. Hoy días  a través de la aplicación podremos consultar la asistencia,
						 saldos, clases, actividades especiales y un sinfín de funcionalidades adicionales. 
						</p>
						
						<div class="col-md-12 padding-top-bottom-20">
							<img class="img-responsive img-center" src="img/software-caracteristicas-easydance.jpg"> 
						</div>
						
						<h4 class="padding-top-20">
							Características y ventajas de la aplicación
						</h4>
						
						<p class="text-justify">
						Definitivamente la aplicación reúne una gran cantidad de aportes y ventajas, pues además de automatizar los procesos cuenta con un PLUS de muy
						alto nivel, el cual, es que la aplicación fue desarrollada exclusivamente con base en la web (en la nube ), desde cualquier dispositivo y en
						cualquier parte en que te encuentres podrás generar respuesta y reportes en tiempo real, manteniendo la información respaldada. A continuación
						presentaremos una de las características más destacadas de la aplicación: 
						</p>
						  
						<div class="padding-top-bottom-20">
							  <p><i class="fa fa-check color-violeta-intermedio"></i> Personalización de la herramienta .</p>
							  <p><i class="fa fa-check color-violeta-intermedio"></i> Módulos administrativos general.</p>
							  <p><i class="fa fa-check color-violeta-intermedio"></i> Módulos de eventos, clases grupales y privadas, actividades especiales entre otros.</p>
