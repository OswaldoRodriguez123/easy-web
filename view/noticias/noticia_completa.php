						Hoy día el mundo ha cambiado, lo que anteriormente se manejaba en papel y lápiz ya hoy se ha convertido en información directa y en tiempo real
						en nuestros dispositivos tecnológicos y en Easy Dance estamos conscientes de eso, por esa razón,en nuestro equipo vimos una oportunidad y
						emprendimos con positivismo, organización y mucho estudio...      
