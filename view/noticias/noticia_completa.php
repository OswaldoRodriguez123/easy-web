						<p class="padding-top-10 text-justify">
						Cabe destacar que esta aplicación será comercializada bajo la filosofía del E Commerce (Comercio electrónico) debemos resaltar que en Venezuela,
						son muy pocas las personas que han emprendido con este modelo de negocio, pero hoy nosotros damos este paso importante para demostrar que contamos
						con emprendedores capaces de crear propuesta de un alto nivel internacional, empresas en el exterior como Amazon, Easy Taxy, Ebay, Sprout social, Mercado libre,
						son un ejemplo tangible y que han trazado la ruta para que muchos emprendedores iniciemos nuestros  proyectos  basados  en la Web. Tema que nos llena de muchas
						expectativas y satisfacción pues no sólo aplicamos una nueva filosofía laboral, sino que somos los pioneros  en latino América, ofreciendo una
						propuesta novedosa para el gremio artístico.    
						</p>
						
						<p class="padding-top-10 text-justify">
						Robert Virona además de dirigir su academias de baile, es un apasionado  de los nuevos  temas de Marketing digital, en la comprensión de la
						conducta de usuarios que navegan en la Web, temas tales como, SEO, SEM CRM, branding, Inbound Marketing y otros relacionados a la tecnología,
						de esta forma lo ha llevado a conformar su empresa basada en la filosofía del E – COMMERCE, dando un paso de envergadura y lanzando un mensaje
						a latino América de manera contundente, los Venezolanos somos emprendedores, muy capaces a un nivel emprendimiento muy alto.      
						</p>
						
