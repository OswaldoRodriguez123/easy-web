						<h4 class="padding-top-20">
							¿Cómo inició el proyecto?  
						</h4>
						
						<p class="text-justify">
						Desde el año  2012 inició un proceso creativo y de diseño a través de un emprendedor Venezolano del estado Zulia (Maracaibo) llamado 
						Robert Virona. Director de la academia de salsa casino Habana Maracaibo  y presidente de la Asociación zuliana  de salsa casino del 
						Estado Zulia AZUCA, en el cual, su propuesta principal fue desarrollar una aplicación que diera funcionalidad a toda la gestión 
						administrativa y organizativa de todos aquellos que hacen vida en el arte, como directores de academias de baile, instructores y bailarines.  
						</p>
						
						<p class="padding-top-10 text-justify">
						Esta aplicación fue culminada en mayo en el año 2014, titulada en ese momento, sistema de academias de baile experto ( SACBEX ), Herramienta
						que no fue expuesta debido a que presentaba diversas fallas técnicas y por tal motivo no salió al mercado, adicional a esto la herramienta
						para ese entonces fue desarrollada en una plataforma local, tema de mucha relevancia, pues el emprendedor al analizar las poderosas ventajas
						que ofrece hoy día la tecnología en la Nube, tomó la acertada decisión de iniciar de nuevo el proyecto desde cero (0) pero en una plataforma
						en la Web, de esta forma retoma el proyecto en enero del año 2015 y ya con sus propuestas más esclarecidas y apoyándose en los estudios de
						las nuevas tendencias tecnológicas, integra al equipo de trabajo  a los ingenieros Juan Delgadillo y Alejandro García, ambos expertos en 
						desarrollar aplicaciones tecnológicas. Bajo la dirección de su creador intelectual Robert Virona inició lo que hoy día es llamado EASY DANCE.   
						</p>
						
						<p class="padding-top-10 text-justify">
						<i>“hoy el equipo de trabajo  cuenta con más de diez integrante, nos sentimos gratamente orgullosos ya que  hemos podido diseñar la primera y única
						herramienta en Latinoamérica que reúne condiciones de muy alto nivel tecnológico, con sello venezolano listo para exportar”</i>  
						</p>
						
						<div class="col-md-12 padding-top-bottom-20">
