							  <p><i class="fa fa-check color-violeta-intermedio"></i> Mensajería telefónica y de correos masivos para Marketing.</p>
							  <p><i class="fa fa-check color-violeta-intermedio"></i> Control de asistencia.</p>
							  <p><i class="fa fa-check color-violeta-intermedio"></i> Soporte técnico.</p>
