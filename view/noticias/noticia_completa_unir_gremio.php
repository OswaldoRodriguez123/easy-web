						<h4 class="padding-top-10 text-center">
						    Uniremos  al gremio.
						</h4>
						<p class="padding-top-20 text-justify">
						Desde ya inició la creación de la herramienta que nos permitirá unir al gremio de baile, aprovechando el uso de la tecnología ,la aplicación que ha iniciado su etapa , se refiere a que todo usuario que desee encontrar una academia de baile , instructor para clases personalizadas, crear citas on line ,   presupuesto de baile , ubicar shows para empresas , animación de fiestas y otras actividades ,  podrán  gestionarse  con sólo un clic desde su dispositivo . 
						</p>
                                                <p class="padding-top-10 text-justify">
						Para lograr este proceso el equipo de trabajado ha realizado un profundo Benchmarking (estudio de mercado) de las mejores herramientas actuales del mercado,C2C (Consumer-to-consumer) es la abreviatura utilizada en comercio electrónico para definir un servicio  de cliente a cliente. 
						</p>
						<h4 class="padding-top-10">
						    ¿Cómo funciona?
						</h4>
                                                <p class="padding-top-10 text-justify">
						Todos los instructores, coreógrafos , bailarines y academias de baile  podrán subir a la plataforma su perfil y ofrecerán sus servicios ,  de esa forma los usuarios en general tendrán acceso a la interfaz y podrán gestionar múltiples funcionalidades , desde presupuestos , hasta inscripciones On-line. 
						</p>
                                                <p class="padding-top-10 text-justify">
						Con  las propuestas que se encuentran en etapa de desarrollo, la aplicación se convertirá en un elemento de uso esencial para el gremio del baile, de esta manera reafirmamos el compromiso de  nuestra start –up (empresa) con el ecosistema  del baile.  
						</p>
                                                <p class="padding-top-10 text-justify">
						El director del proyecto Robert Virona es quien lidera la visión y la plasma en maquetas de desarrollo, llamadas Mock –ups ,  comenta además, que se siente muy satisfecho debido a que el equipo de programadores son de muy alto nivel,“no me ha hecho falta viajar  a silicon valley a buscar programadores que dominen  herramientas como las que hoy día se manejan en Facebook , twitter entre otros ,ya que en Easy dance contamos con  un equipo de programación  de muy alto nivel de conocimientos y ejecución, esto nos garantiza un alto poder de escalabilidad  ” Comentó el emprendedor.  
						</p>
                                                
