						<p class="padding-top-20 text-justify">
						El prestigioso programa de Start- up chile, es el evento que se celebra todos los años  en SANTIAGO DE CHILE , en este participan  más de veinte (20) países distintos y un aproximado de mil propuestas que arriban de todas parte del mundo, actualmente es considerada como  la comunidad más  diversas de empresarios y start- up (empresas) en todo el continente latinoamericano. 
						</p>
                                                <p class="padding-top-10 text-justify">
						Chile se ha convertido en el centro de una de las comunidades más  grandes de las start ups en toda Latinoamérica, las alianzas generadas con grandes inversionistas, asesores y programas de enseñanzas, hace que despierte en el sentimiento del emprendedor un deseo inmedible de ser parte de esa fascinante comunidad del área tecnológica y de negocios 
						</p>
                                                <p class="padding-top-10 text-justify">
						Los emprendedores venezolanos creadores de la aplicación  Easy dance participarán en el prestigioso programa  para este mes de febrero, en el que aspiran ser seleccionado para el programa semilla a celebrase en la capital chilena. 
						</p>
                                                <p class="padding-top-10 text-justify">
						 CORFO, a través de su Comité Innova Chile, creó desde el año 2010,  la Línea de Financiamiento Start-Up Chile, en el que brinda  el  apoyo económico , más la participación de diversos eventos entre grandes talentos y empresarios de diferentes parte del mundo. Los emprendedores seleccionados deberán estar presente durante seis meses de formación en Santiago de chile, en el que serán asesorados por profesionales de diversas áreas del sector , Para  aplicar en la selección  del programa, la organización CORFO , posee unos altos estándares  de criterios evaluativos en el que se someten cada uno de los aspirantes.
						</p>
                                                <p class="padding-top-10 text-justify">
						El equipo Easy dance a pesar de encontrase en una etapa muy temprana  de lanzamiento, ha logrado presentar su proyecto ante los grandes emprendedores mundiales de  países como estados unidos, Chile, Argentina Italia, México, Colombia, España entre otros , con un alto grado de nivel en emprendimientos se sumarán a la fiesta competitiva ,el día 15 de febrero el equipo se postulará y actualmente se  prepara en  su primer gran reto internacional. 
						</p>
                                                
