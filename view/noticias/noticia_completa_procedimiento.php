                                                <h4 class="padding-top-20">
						    Ventajas de crear un manual de procesos
						</h4>
                                                <p class="padding-top-10 text-justify">
						Se crea una línea y filosofía laboral / Sirve como administrador de funciones para el equipo de trabajo y  colaboradores 
                                                / Permite evaluar la  operatividad de manera objetiva / Facilita la supervisión y entrega de funciones. 
						</p>
                                                <p class="padding-top-10 text-justify">
						Los procesos no son definitivos ni lineales, a medida que vaya mejorando tu organización estos deben ir generando cambios
                                                y de esa forma irlos fortaleciendo. La herramienta Easy Dance, ofrecerá un espacio para que puedas desarrollar tus 
                                                manuales de procedimiento y de esta forma contribuya con tu organización. 
						</p>
                                                <p class="padding-top-10 text-justify">
						A continuación deseo compartir  un ejemplo de un manual de procedimientos para sus  equipos  de instructores, espero les sirva de orientación y les motive a construir el suyo. 
						</p>
                                                
                                                <h3 class="padding-top-20 text-center" >MANUAL DE PROCEDIMIENTOS</h3>
						
                                                <p class="padding-top-10" ><b>NOMBRE DEL PUESTO:</b> Instructor</p>  

                                                <p class="padding-top-10" ><b>PUESTO DEL QUE DEPENDE JERÁRQUICAMENTE:</b> Director General y coordinador De pista </p>

                                                <p class="padding-top-10" ><b>PUESTO(S) QUE SUPERVISA DIRECTAMENTE:</b> Ninguno </p>
                                                
                                                <h4 class="padding-top-20 text-center" >II. Descripción General del Puesto</h4>
                                                
                                                <p class="padding-top-10 text-justify">
                                                Responsable de las clases de baile que realiza, motivando e incentivando a los  alumnos con liderazgo y buena aptitud en su desempeño 
                                                </p>
                                                
                                                <p class="padding-top-10">
                                                    <b>Esquema de funciones diarias</b>  
                                                </p>
                                                
                                                <div class="padding-10">
						    <p> 1. Hora de llegada</p>
                                                    <p> 2. Ambiente</p> 
                                                    <p> 3. Uniforme</p> 
                                                    <p> 4. Música</p> 
                                                    <p> 5. Tiempo de descanso</p> 
                                                    <p> 6. Culminación de la clase</p> 
                                                    <p> 7. Limpieza de la pista</p>
                                                    <p> 8. Información</p>
						</div>
                                                
                                                <h4 class="padding-top-20 text-center" >DESARROLLO</h4>
                                                
                                                <p class="padding-top-10 text-justify"><b>1. Hora de llegada : </b>el instructor  al momento de su llegada, deberá firmar su asistencia para así constatar
                                                    su horario de entrada , este debe ser 15 minutos antes de su clase.
                                                </p>    


                                                <p class="padding-top-10 text-justify"><b>2. Ambiente:</b> 
                                                    el instructor debe asegurarse que la pista se encuentre en óptimas condiciones, procediendo al uso 
                                                    del ambientador  del salón, además colocando música de fondo acorde a la clase. 
                                                </p>
                                                
                                                <p class="padding-top-10 text-justify"><b>3. Uniforme:</b>  
                                                    el instructor debe presentarse con uniforme en buenas condiciones, limpio, de olor agradable, entro otros.
                                                </p>

                                                <p class="padding-top-10 text-justify"><b>4. Música:</b>ubicada en la PC, esta deberá ser acorde y a nivel a los grupos que
                                                    participan en su clase ,sólo el instructor está autorizado del manejo de la misma dentro del salón de clases
                                                </p>
                                                
                                                <p class="padding-top-10 text-justify"><b>5. Tiempo de descanso:</b> este durará no más de 5 minutos, a la mitad de la clase, 
                                                    el instructor aprovechará ese espacio para sociabilizar con los alumnos, hablarles de los eventos e informaciones
                                                    pertinentes de la semana.
                                                </p>

                                                <p class="padding-top-10 text-justify"><b>6. Culminación de la clase:</b> el instructor procederá a culminar su faena 5 minutos antes, para interactuar con ellos una vez más e incentivándolos
                                                    a participar en las nuevas clases y actividades futura de la academia.
                                                </p>

                                                <p class="padding-top-10 text-justify"><b>7. Limpieza de la pista:</b> el instructor es garante de que los alumnos, no consuman alimentos dentro de la pista de baile, 
                                                    así mismo promoverá la limpieza del salón, para hacer entrega de una pista de baile acorde a las clase siguiente. 
                                                </p>
                                                
                                                <p class="padding-top-10 text-justify"><b>8. Información:</b> orientan a nuestros alumnos de las actividades que se realizan dentro de la academia,
                                                    manteniendo informados de eventos, ensayos, prácticas y otros que enrumben a la motivación de nuestros alumnos, 
                                                    esta deberá realizarse dos veces por clase.
                                                </p>
                                                
