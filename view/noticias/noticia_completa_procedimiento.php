						<h4 class="padding-top-10 text-center">
						    Sin manuales de procedimiento para tu academia de baile no hay crecimiento.
						</h4>
						<p class="padding-top-20 text-justify">
						Si tu has escuchado  el significado de los Manuales de procedimientos de una empresa, evento u actividades 
                                                que realizas, pero aun no has creado el tuyo, te invito a que no dejes de leer  este artículo... 
						</p>
                                                <p class="padding-top-10 text-justify">
						Los manuales de procedimiento es una herramienta sumamente fundamental para toda organización, desde 
                                                grandes corporaciones a pequeños  empresarios, sin esta herramienta sería imposible manejar una 
                                                organización de manera efectiva. 
						</p>
                                                <p class="padding-top-10 text-justify">
						Los manuales de los procedimientos  son los procesos que todas las  empresas  deben poseer, pero para entender
                                                cómo desarrollarlos iniciaremos con una breve explicación del significado de los mismos. 
						</p>
                                                <p class="padding-top-10 text-justify">
						Estos se refieren a documentar de manera sencilla, organizada, sistemática y metódica todos los pasos a seguir 
                                                que el  director plantea como ideales y políticas laborales dentro de su empresa, en los diversos departamentos 
                                                que posee. 
						</p>
                                                <p class="padding-top-10 text-justify">
						En el caso de los directores de academias de baile  por muy pequeña que sea, se necesita de igual manera crear sus procesos,
                                                habitualmente en las academias existen diversas áreas o departamentos  con sus particularidades como por ejemplo,
                                                procesos de  como dictar una clase de baile  o como   preparar una coreografía, departamento de recepción, compañía
                                                de baile entre otros. 
						</p>
                                                
                                                <div class="col-md-12 padding-top-bottom-20">
						    <img class="img-responsive img-center" src="img/noticias/procedimientos.jpg"> 
